  <h1>History Of CNSHS</h1><br>
  <p align="justify">By virtue of the Republic Act No. 10533 known as the “Enhanced Basic Education Act of 2013” which was signed on May 15, 2013 by former president, the Hon. Benigno S. Aquino III, the curriculum was strengthened and the number of years for basic education was increased by two years. As pursuant to this, the School Principal IV of Cabiao National High School, Lorenzo P. Joaquin, and the 30 newly appointed teachers joined hands in the first year implementation of SHS during the School Year 2016-2017. With eight classrooms given by the Department of Education and additional six classrooms from Governor Aurelio M. Umali, the senior high school was launched and offered strands under Academic Track and Technical Vocational Livelihood Track. Cabiao National High School catered the first batch of Grade 11 students with the total population of 846. The population of the pioneer batch waved for the Senior High School Department to be the largest public senior high school in Central Luzon. Later, the division office deployed three assistant school principals – Jessica P. Francisco for Operations and Learners Support, Reynaldo E. Francisco, Ph. D. for Academics and Romulo P. Vijandre for Junior High School on July 28, 2016. Since Jessica P. Francisco was promoted to School Principal I on July 18, 2017, Ronaldo U. Dayao took over the position of Assistant School Principal II for Operations and Learners Support on September 18, 2017. Through the continuous support and guidance of Ronaldo A. Pozon, Ph. D., Schools Division Superintendent; Johanna N. Gervacio, Ph. D., Assistant Schools Division Superintendent - AID, Ericson S. Sabacan, Ed. D., Assistant Schools Division Superintendent - LMD; Erwin T. Donelo, Senior High School Division Focal Person, and the Hon. Ramil B. Rivera, Municipal Mayor of Cabiao, facilities, tools and materials needed per strand and additional teachers were granted.The school also availed additional 60 classrooms. Nevertheless, the time has come for the growing senior high school to have a separate school principal when the division office deployed the school’s first principal, Perfecto C. Felix Jr., on May 2, 2017. In accordance to DepEd Order No. 51, s. 2015 that was signed on October 29, 2015, from being an integrated school, Cabiao National Senior High School was established as one of the four stand-alone senior high schools in the Division of Nueva Ecija. Cabiao National Senior High School through the leadership of the School Principal II, Perfecto C. Felix Jr., and through the support of two (2) assistant school principals, 51 teachers, 12 non-teaching personnel, and 1,615 students inaugurated these three 4-storey 20 CL buildings on August 8, 2018 in the new site in Barangay Palasinan, Cabiao, Nueva Ecija during the 3rd Foundation Day Celebration.</p>
